#Data Types

# Subscripting = The method of pulling out a particular element from a string
print("Hello"[0])

# String
print("123" + "456")       # It will concatenate these two strings

# Integers = Whole number
print(123+456)             # It will add these two numbers

# Large Integers
print(123_456_789)         # Output 123456789
# 342,653,987  In Python , we can replace the commas with underscore like this
# 342_653_987 ,  it will be interpreted by the computer as 342654897
# The benefit is just for us human to be able to visualize it more easily.

#Float = Floating Point Number
print(3.14159)

#Boolean = Two Possible values either true or false
print(True)
print(False)